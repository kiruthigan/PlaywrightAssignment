// Step 1: Declare and initialize the variable
let num = -5;

// Step 2 & 3: Use conditional statements and assign result
let result;

if (num > 0) {
    result = "Positive number";
} else if (num < 0) {
    result = "Negative number";
} else {
    result = "Zero";
}

// Step 4: Print the result
console.log(result);